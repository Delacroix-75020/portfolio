                    <p class="lead">
                        L’Office français de l'immigration et de l'intégration (OFII) est un établissement public à caractère administratif chargé d'organiser l'accueil des demandeurs d'asile et des réfugiés sur le sol français. Créé en mars 2009 par la fusion de plusieurs agences nationales, il est placé sous la tutelle du ministère de l'Intérieur</br> </br>
                        L'OFII a en outre pour missions la gestion des procédures de l'immigration professionnelle et familiale, la gestion du dispositif national d'accueil des demandeurs d'asile, celle des aides au retour et à la réinsertion participant au développement solidaire, ainsi que la lutte contre le travail illégal.</br> </br>

                        L’OFII est représenté par 53 directions territoriales, délégations ou plateformes hébergées qui permettent de couvrir l’ensemble du territoire national. À l’étranger, l’OFII est représenté dans sept pays : Maroc, Tunisie, Turquie, Mali, Sénégal, Cameroun et Arménie.</p>
